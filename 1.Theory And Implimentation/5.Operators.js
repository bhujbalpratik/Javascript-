/*
            Increment OR Decrement Operator
            ++ for Increment 
            -- for Decrement
            ++a pre-increment
            a-- post-decrement

        pre
            first increment/decrement value and then assign.

        post 
            first assign value then increment/decrement
        
        var a=10;
        a++;
        a--;
        document.getElementById("result").innerHTML= a;
        */

